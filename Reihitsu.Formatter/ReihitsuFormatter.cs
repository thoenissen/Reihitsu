using System.Threading.Tasks;

using Microsoft.CodeAnalysis;

using Reihitsu.Formatter.Pipeline;

namespace Reihitsu.Formatter;

/// <summary>
/// Reihitsu code formatter
/// </summary>
public static class ReihitsuFormatter
{
    #region Methods

    /// <summary>
    /// Formats an entire document by applying all formatting rules
    /// </summary>
    /// <param name="document">The Roslyn Document to format</param>
    /// <param name="cancellationToken">Cancellation token</param>
    /// <returns>A new Document with formatting applied</returns>
    public static async Task<Document> FormatDocumentAsync(Document document, CancellationToken cancellationToken = default)
    {
        var syntaxTree = await document.GetSyntaxTreeAsync(cancellationToken).ConfigureAwait(false);

        if (syntaxTree == null)
        {
            return document;
        }

        var root = await syntaxTree.GetRootAsync(cancellationToken).ConfigureAwait(false);

        if (ReihitsuFormatterHelpers.HasSyntaxErrors(syntaxTree))
        {
            return document;
        }

        if (ReihitsuFormatterHelpers.IsAutoGeneratedSource(root))
        {
            return document;
        }

        var endOfLine = ReihitsuFormatterHelpers.DetectEndOfLine(root);
        var context = new FormattingContext(endOfLine);
        var formattedRoot = FormattingPipeline.Execute(root, context, cancellationToken);

        return document.WithSyntaxRoot(formattedRoot);
    }

    /// <summary>
    /// Formats a syntax tree by applying all formatting rules.
    /// Use this overload when no Workspace/Document context is available
    /// </summary>
    /// <param name="syntaxTree">The syntax tree to format</param>
    /// <param name="cancellationToken">Cancellation token</param>
    /// <returns>A new SyntaxTree with formatting applied</returns>
    public static SyntaxTree FormatSyntaxTree(SyntaxTree syntaxTree, CancellationToken cancellationToken = default)
    {
        if (ReihitsuFormatterHelpers.HasSyntaxErrors(syntaxTree))
        {
            return syntaxTree;
        }

        var root = syntaxTree.GetRoot(cancellationToken);

        if (ReihitsuFormatterHelpers.IsAutoGeneratedSource(root))
        {
            return syntaxTree;
        }

        var endOfLine = ReihitsuFormatterHelpers.DetectEndOfLine(root);
        var context = new FormattingContext(endOfLine);
        var formattedRoot = FormattingPipeline.Execute(root, context, cancellationToken);

        return syntaxTree.WithRootAndOptions(formattedRoot, syntaxTree.Options);
    }

    /// <summary>
    /// Formats a specific syntax node and its descendants.
    /// Useful for code fix providers that need to format only a
    /// newly generated or modified node rather than the full document
    /// </summary>
    /// <param name="node">The syntax node to format</param>
    /// <param name="indentLevel">
    /// The indentation level of the node within its containing document.
    /// Required for newly generated nodes that are not yet inserted into a tree,
    /// where the indentation level cannot be inferred from position
    /// </param>
    /// <param name="cancellationToken">Cancellation token</param>
    /// <returns>A new SyntaxNode with formatting applied</returns>
    public static SyntaxNode FormatNode(SyntaxNode node, int indentLevel = -1, CancellationToken cancellationToken = default)
    {
        var endOfLine = ReihitsuFormatterHelpers.DetectEndOfLine(node);
        var baseIndentLevel = indentLevel >= 0 ? indentLevel : ReihitsuFormatterHelpers.ComputeBaseIndentLevel(node);
        var context = new FormattingContext(endOfLine, baseIndentLevel);

        return FormattingPipeline.Execute(node, context, cancellationToken);
    }

    /// <summary>
    /// Formats a specific syntax node using document-derived indentation and line-ending context.
    /// Only the targeted node is rewritten back into the document; callers that intend document-wide
    /// formatting should use <see cref="FormatDocumentAsync(Document, CancellationToken)"/> instead
    /// </summary>
    /// <param name="document">The Roslyn Document containing the target node</param>
    /// <param name="targetNode">The syntax node to format. Must belong to the document's syntax tree</param>
    /// <param name="cancellationToken">Cancellation token</param>
    /// <returns>A new Document with only the targeted node formatted</returns>
    public static async Task<Document> FormatNodeInDocumentAsync(Document document, SyntaxNode targetNode, CancellationToken cancellationToken = default)
    {
        var syntaxTree = await document.GetSyntaxTreeAsync(cancellationToken).ConfigureAwait(false);

        if (syntaxTree == null)
        {
            return document;
        }

        var root = await syntaxTree.GetRootAsync(cancellationToken).ConfigureAwait(false);

        if (ReihitsuFormatterHelpers.HasSyntaxErrors(syntaxTree))
        {
            return document;
        }

        if (ReihitsuFormatterHelpers.IsAutoGeneratedSource(root))
        {
            return document;
        }

        var originalFirstToken = targetNode.GetFirstToken();
        var originalColumn = ReihitsuFormatterHelpers.ComputeTokenColumn(originalFirstToken, root);
        var endOfLine = ReihitsuFormatterHelpers.DetectEndOfLine(root);
        var baseIndentLevel = ReihitsuFormatterHelpers.ComputeBaseIndentLevel(targetNode);
        var context = new FormattingContext(endOfLine, baseIndentLevel);
        var formattedTarget = FormattingPipeline.Execute(targetNode, context, cancellationToken);
        var formattedColumn = ReihitsuFormatterHelpers.ComputeTokenColumn(formattedTarget.GetFirstToken(), formattedTarget);
        var columnOffset = originalColumn - formattedColumn;

        if (columnOffset != 0)
        {
            formattedTarget = ReihitsuFormatterHelpers.AdjustNodeIndentation(formattedTarget, columnOffset);
        }

        if (ReihitsuFormatterHelpers.StartsOnNewLine(originalFirstToken) == false)
        {
            var formattedFirstToken = formattedTarget.GetFirstToken();
            formattedTarget = formattedTarget.ReplaceToken(formattedFirstToken, formattedFirstToken.WithLeadingTrivia(originalFirstToken.LeadingTrivia));
        }

        if (targetNode == root)
        {
            return document.WithSyntaxRoot(formattedTarget);
        }

        var originalLastToken = targetNode.GetLastToken();
        var formattedLastToken = formattedTarget.GetLastToken();
        formattedTarget = formattedTarget.ReplaceToken(formattedLastToken, formattedLastToken.WithTrailingTrivia(originalLastToken.TrailingTrivia));

        return document.WithSyntaxRoot(root.ReplaceNode(targetNode, formattedTarget));
    }

    #endregion // Methods
}